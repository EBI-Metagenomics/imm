static void msv3000_path(void)
{
  int core_size = 3000;
  int pfam_core_size = 3258786;
  imm_ex3_init(core_size);

  struct imm_ex3 *m = &imm_ex3;
  struct imm_dp dp;
  imm_hmm_init_dp(imm_ex3.hmm, &dp);
  struct imm_task *task = imm_task_new(&dp);
  struct imm_prod prod = imm_prod();

  struct imm_eseq eseq = {0};
  imm_eseq_init(&eseq, &m->code);

  // struct imm_seq seq = imm_seq(imm_str(seq1), &m->abc);
  struct imm_seq seq = imm_seq_unsafe(imm_str(seq10), &m->abc);
  eq(imm_eseq_setup(&eseq, &seq), 0);
  eq(imm_task_setup(task, &eseq), 0);

  eq(imm_dp_viterbi(&dp, task, &prod), 0);
  near(prod.loglik, -216494.453125);
  printf("MSV-path [N=%u, M=%u]: %llu msecs\n", imm_seq_size(&seq), core_size,
         (int long long)prod.mseconds);
  printf("  [N=%u, M=Pfam[%u]]: %.1f mins\n", imm_seq_size(&seq),
         pfam_core_size,
         (pfam_core_size / ((double)core_size)) * (double)prod.mseconds /
             1000. / 60.);

  imm_eseq_cleanup(&eseq);
  imm_task_del(task);
  imm_prod_cleanup(&prod);
  imm_dp_cleanup(&dp);
  imm_ex3_cleanup();
}

